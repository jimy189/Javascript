var x = 0;

while(x < 10){
    document.writeln('x = ',x);
    document.writeln('<br/>');
    x++;
}

var y = 0;
do{
    document.writeln('y = ',y);
    document.writeln('<br/>');
    y++;
}while(y < 10);

for(var i = 0; i < 10; i++){
    document.writeln('i = ',i);
    document.writeln('<br/>');
}

